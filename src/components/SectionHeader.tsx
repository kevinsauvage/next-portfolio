import type { ReactNode } from 'react';

import { BodyLarge, H2, Overline } from '@/components/Typography';
import { stackSpacing } from '@/design-system/tokens';

import clsx from 'clsx';

type SectionHeaderProps = {
  overline?: string;
  title: string | ReactNode;
  description?: string | ReactNode;
  className?: string;
  gradient?: boolean;
  align?: 'left' | 'center';
};

export const SectionHeader = ({
  overline,
  title,
  description,
  className,
  gradient = true,
  align = 'left',
}: SectionHeaderProps) => {
  const alignmentClass = align === 'center' ? 'text-center' : 'text-left';

  return (
    <div className={clsx(stackSpacing.md, alignmentClass, className)}>
      {overline && <Overline>{overline}</Overline>}

      <H2
        gradient={gradient}
        className='animate-gradient bg-[length:200%_auto] text-4xl md:text-5xl lg:text-6xl'
      >
        {title}
      </H2>

      {description && <BodyLarge className='max-w-4xl'>{description}</BodyLarge>}
    </div>
  );
};
